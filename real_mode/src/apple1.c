Byte apple1_read(void* context, Word address) {
    Apple1* sys = (Apple1*)context;
    
    if (address < 0x2000) return sys->ram[address];
    
    // I/O: 读取键盘数据
    if (address == 0xD010) {
        Byte data = sys->kbd_data;
        sys->kbd_ready = 0; // 清除状态
        return data | 0x80; // 最高位置 1
    }
    // I/O: 读取键盘状态寄存器
    if (address == 0xD011) {
        return sys->kbd_ready ? 0x80 : 0x00;
    }
    // I/O: 读取显示器状态
    if (address == 0xD013) {
        return 0x80; // 屏幕准备就绪
    }
    // ROM 读取
    if (address >= 0xFF00) {
        return sys->rom[address - 0xFF00];
    }
    
    return 0xFF;
}

void apple1_write(void* context, Word address, Byte value) {
    Apple1* sys = (Apple1*)context;
    
    if (address < 0x2000) {
        sys->ram[address] = value;
    }
    // I/O: CPU 向屏幕输出字符
    else if (address == 0xD012) {
        char c = value & 0x7F; // 去掉高位屏蔽
        if (c == '\r') printf("\n");
        else           printf("%c", c);
        fflush(stdout);
    }
}